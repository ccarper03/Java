public class Demo01c 
{
	public static void main(String[] args) 
	{
		String strOne, strTwo, strThree; 		// Declare variables  	    			    	
		strOne = "Java";						// Populate variable
    	strTwo = "Programming";				
		strThree = "Fundamentals";					
    	System.out.print(strOne);				// Display variable value
		System.out.print(strTwo);
		System.out.print(strThree);
		System.out.println();					// Display a blank line
		System.out.println(strOne);
		System.out.println(strTwo);
		System.out.println(strThree);
		System.out.println();
		System.out.println("End of Program.");
	}
}


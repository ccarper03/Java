public class Chap01b 
{

	public static void main(String[] args) 
	{
		System.out.println("20 + 3 = " + (20 + 3));
		System.out.println("Math performed because the numbers (in parentheses) are not enclosed in quotes.");
		System.out.println();
		System.out.println("20 + 3");
		System.out.println("No math performed because the numbers are enclosed in quotes.");
		System.out.println();
		System.out.println("End of Program");
	}

}

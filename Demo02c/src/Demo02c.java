
public class Demo02c 
{

	public static void main(String[] args)
	{
		int int1 = 3, int2 = 6, int3 = 9;
		System.out.println("The int1 variable was assigned the initial value " + int1 + ".");
		System.out.println("The int2 variable was assigned the initial value " + int2 + ".");
		System.out.println("The int3 variable was assigned the initial value " + int3 + ".");
		System.out.println();
		int1++;
		int2 = int2 + 4;
		int3 = int3 + 6;
		System.out.println("The int1 variable now contains the value " + int1 + ".");
		System.out.println("The int2 variable now contains the value " + int2 + ".");
		System.out.println("The int3 variable now contains the value " + int3 + ".");
		System.out.println();
		int1--;
		int2 = int2 - 5;
		int3 = int3 - 3;
		System.out.println("The int1 variable now contains the value " + int1 + ".");
		System.out.println("The int2 variable now contains the value " + int2 + ".");
		System.out.println("The int3 variable now contains the value " + int3 + ".");
		System.out.println();
		System.out.println("End of Program.");


	}

}


public class Demo02b 
{

	public static void main(String[] args) 
	{
		int numOne; 								// Declare int variable numOne
      	numOne = 7; 								// Populate numOne with  7
      	System.out.println(numOne);       			// Displays 7
      	System.out.println(numOne + 4);   			// Displays 11
      	System.out.println(numOne);       			// Displays 7
      	System.out.println("numOne");				// Displays numOne
      	numOne = 20; 								// Populate numOne with  20
      	System.out.println(numOne);  				// Displays 20 
      	numOne = numOne + 5; 						// Add 5 to the value in numOne
      	System.out.println(numOne);  				// Displays 25
      	numOne++; 									// Add 1 to the value in numOne
      	System.out.println("numOne = " + numOne); 	// Displays numOne = 26
      	numOne--;									// Subtract 1 from value in numOne
      	System.out.println("numOne = " + numOne); 	// Displays numOne = 25
      	numOne = numOne - 7; 						// Subtract 7 from value in numOne
      	double numTwo, numThree; 	 				// Declare  double variable s numTwo and numThree
      	numTwo = 5.0; 			 					// Populate numTwo with  5.0
      	numTwo = numTwo / 2.0; 						// Populate numTwo  with division of numTwo by  2.0
      	System.out.println(numTwo);			  		// Displays 2.5 
      	numThree = numTwo / 2.0; 					// Populate numThree with division of numTwo by 2.0
      	System.out.println(numThree);  				// Displays 1.25


	}

}
